# Complete the rotLeft function below.
def rotLeft(a, d):
    rotator = collections.deque(a)
    rotator.rotate(0-abs(d)) #Make d negative in order to rotate left
    return list(rotator)

if __name__ == '__main__':
    fptr = open(os.environ['OUTPUT_PATH'], 'w')

    nd = input().split()

    n = int(nd[0])

    d = int(nd[1])

    a = list(map(int, input().rstrip().split()))

    result = rotLeft(a, d)

